const { createApp } = Vue;

createApp({
    data() {
        return {
            activeTab: 'sigma',
            searchQuery: '',
            selectedTactic: '',
            selectedSeverity: '',
            selectedRule: null,
            sigmaRules: [],
            yaraRules: [],
            loading: true,
            tactics: [],
            uniqueTechniques: []
        };
    },

    computed: {
        filteredSigmaRules() {
            let filtered = this.sigmaRules;

            if (this.searchQuery) {
                const query = this.searchQuery.toLowerCase();
                filtered = filtered.filter(rule =>
                    rule.title.toLowerCase().includes(query) ||
                    rule.description.toLowerCase().includes(query) ||
                    rule.technique.toLowerCase().includes(query)
                );
            }

            if (this.selectedTactic) {
                filtered = filtered.filter(rule =>
                    rule.tactic.toLowerCase().includes(this.selectedTactic.toLowerCase())
                );
            }

            if (this.selectedSeverity) {
                filtered = filtered.filter(rule =>
                    rule.level === this.selectedSeverity
                );
            }

            return filtered;
        },

        filteredYaraRules() {
            if (!this.searchQuery) return this.yaraRules;

            const query = this.searchQuery.toLowerCase();
            return this.yaraRules.filter(rule =>
                rule.name.toLowerCase().includes(query) ||
                rule.description.toLowerCase().includes(query) ||
                (rule.mitre_attack && rule.mitre_attack.toLowerCase().includes(query))
            );
        },

        totalRules() {
            return this.sigmaRules.length + this.yaraRules.length;
        },

        coveragePercentage() {
            const totalMitreTechniques = 400;
            return Math.round((this.uniqueTechniques.length / totalMitreTechniques) * 100);
        }
    },

    async mounted() {
        await this.loadRulesDynamically();
        this.loading = false;
    },

    methods: {
        async loadRulesDynamically() {
            try {
                // Discover and load rules from repository structure
                await this.discoverSigmaRules();
                await this.discoverYaraRules();
                this.extractMetadata();
            } catch (error) {
                console.error('Error loading rules:', error);
                this.loadSampleData();
            }
        },

        async discoverSigmaRules() {
            // Get repository file listing (works for GitHub Pages)
            const sigmaDirectories = [
                '02 Resource Development',
                '03 Initial Access',
                '04 Execution',
                '05 Defense Evasion',
                '05 Persistence',
                '06 Privilege Escalation',
                '07 Defense Evasion',
                '08 Credential Access',
                '09 Discovery',
                '10 Lateral Movement',
                '12 Command and Control',
                '13 Exfiltration',
                '14 Impact'
            ];

            for (const dir of sigmaDirectories) {
                try {
                    await this.loadSigmaFromDirectory(dir);
                } catch (error) {
                    console.error(`Critical error loading directory ${dir}:`, error.message);
                    // Continue with other directories - don't let one failure break the entire app
                    continue;
                }
            }
        },

        async loadSigmaFromDirectory(directory) {
            // Dynamic basePath for localhost development and GitHub Pages
            const basePath = (() => {
                const isLocalhost = window.location.hostname === 'localhost' ||
                                   window.location.hostname === '127.0.0.1';

                if (isLocalhost) {
                    // For localhost, check if server runs from root or docs/
                    return window.location.pathname.includes('/docs/') ? '' : '/docs';
                } else {
                    // GitHub Pages production - use raw content
                    return 'https://raw.githubusercontent.com/infinit3i/Defensive-Rules/main';
                }
            })();

            // Sanitize directory name
            const sanitizedDir = directory.replace(/[^a-zA-Z0-9\s\-]/g, '');

            // Try to load known rule files (this would be generated by build process)
            const ruleFiles = await this.discoverRuleFiles(`Sigma/${sanitizedDir}`);

            if (ruleFiles.length === 0) {
                console.warn(`No rule files discovered in directory: ${directory}`);
                return;
            }

            console.log(`Discovered ${ruleFiles.length} files in ${directory}`);
            let successCount = 0;

            for (const file of ruleFiles) {
                if (!this.isValidRuleFileName(file)) continue;

                try {
                    const isLocalhost = window.location.hostname === 'localhost' ||
                                       window.location.hostname === '127.0.0.1';

                    const url = isLocalhost
                        ? `${basePath}/Sigma/${encodeURIComponent(sanitizedDir)}/${encodeURIComponent(file)}`
                        : `${basePath}/Sigma/${encodeURIComponent(sanitizedDir)}/${encodeURIComponent(file)}`;

                    const response = await fetch(url);
                    if (response.ok) {
                        const yamlContent = await response.text();
                        const rule = this.parseYamlRule(yamlContent, file, directory);
                        if (rule) {
                            this.sigmaRules.push(rule);
                            successCount++;
                        }
                    } else {
                        console.warn(`HTTP ${response.status} for ${file}`);
                    }
                } catch (error) {
                    console.warn(`Failed to load ${file}:`, error.message);
                }
            }

            console.log(`Loaded ${successCount}/${ruleFiles.length} rules from ${directory}`);
        },

        async discoverRuleFiles(directory) {
            // Sanitize directory input - prevent path traversal
            const sanitizedDir = directory.replace(/\.\./g, '').replace(/[^a-zA-Z0-9\/\s\-]/g, '');
            const isLocalhost = window.location.hostname === 'localhost' ||
                               window.location.hostname === '127.0.0.1';

            if (isLocalhost) {
                // For localhost development, try to list directory contents
                try {
                    const basePath = window.location.pathname.includes('/docs/') ? '' : '/docs';
                    const dirUrl = `${basePath}/${sanitizedDir}/`;
                    const response = await fetch(dirUrl);

                    if (response.ok) {
                        const html = await response.text();
                        const files = this.parseDirectoryListing(html);
                        return files.filter(file => this.isValidRuleFileName(file));
                    }
                } catch (error) {
                    console.warn(`Local directory listing failed for ${sanitizedDir}:`, error.message);
                    // Fall back to known files for development
                    return this.getKnownFilesForDirectory(directory);
                }
            } else {
                // Use GitHub API for production
                try {
                    const apiUrl = `https://api.github.com/repos/infinit3i/Defensive-Rules/contents/${encodeURIComponent(sanitizedDir)}`;
                    const response = await fetch(apiUrl);

                    if (response.ok) {
                        const files = await response.json();
                        return files
                            .filter(file => file.type === 'file' && this.isValidRuleFileName(file.name))
                            .map(file => file.name);
                    }
                } catch (error) {
                    console.warn(`GitHub API failed for ${sanitizedDir}:`, error.message);
                }
            }

            return [];
        },

        parseDirectoryListing(html) {
            // Parse simple HTTP directory listing for .yml files
            const files = [];
            const linkRegex = /<a[^>]+href="([^"]+\.yml)"[^>]*>/gi;
            let match;

            while ((match = linkRegex.exec(html)) !== null) {
                const filename = decodeURIComponent(match[1]);
                if (filename.endsWith('.yml') && !filename.includes('/')) {
                    files.push(filename);
                }
            }

            return files;
        },

        getKnownFilesForDirectory(directory) {
            // Fallback to return empty array for development
            // In a real deployment, this could return a pre-built file list
            console.warn(`Using fallback for directory: ${directory}`);
            return [];
        },

        parseYamlRule(yamlContent, filename, directory) {
            try {
                // Simple YAML parser for Sigma rules
                const rule = this.parseSimpleYaml(yamlContent);

                return {
                    id: rule.id || filename,
                    title: rule.title || 'Untitled',
                    status: rule.status || 'experimental',
                    description: this.cleanDescription(rule.description || ''),
                    level: rule.level || 'medium',
                    author: rule.author || 'Unknown',
                    date: rule.date || '',
                    technique: this.extractTechnique(rule.tags || []),
                    tactic: this.mapTacticFromDirectory(directory),
                    tags: rule.tags || [],
                    references: rule.references || [],
                    filename: filename
                };
            } catch (error) {
                console.warn(`Failed to parse ${filename}:`, error);
                return null;
            }
        },

        parseSimpleYaml(yamlContent) {
            // Simple YAML parser for basic Sigma rule structure
            const lines = yamlContent.split('\n');
            const result = {};
            let currentKey = null;
            let currentValue = [];

            for (let line of lines) {
                line = line.trim();

                if (line.startsWith('---') || line === '' || line.startsWith('#')) {
                    continue;
                }

                if (line.includes(':') && !line.startsWith(' ') && !line.startsWith('-')) {
                    if (currentKey) {
                        result[currentKey] = this.processValue(currentValue);
                    }

                    const [key, ...valueParts] = line.split(':');
                    currentKey = key.trim();
                    currentValue = [valueParts.join(':').trim()];
                } else if (currentKey && (line.startsWith(' ') || line.startsWith('-'))) {
                    currentValue.push(line);
                }
            }

            if (currentKey) {
                result[currentKey] = this.processValue(currentValue);
            }

            return result;
        },

        processValue(valueArray) {
            const combined = valueArray.join('\n').trim();

            if (combined.startsWith('[') && combined.endsWith(']')) {
                // Array format
                return combined.slice(1, -1).split(',').map(s => s.trim().replace(/['"]/g, ''));
            }

            if (combined.includes('\n-')) {
                // YAML list
                return combined.split('\n')
                    .filter(line => line.trim().startsWith('-'))
                    .map(line => line.trim().substring(1).trim().replace(/['"]/g, ''));
            }

            return combined.replace(/['"]/g, '');
        },

        extractTechnique(tags) {
            if (!Array.isArray(tags)) return null;

            for (const tag of tags) {
                if (typeof tag === 'string' && tag.startsWith('attack.t')) {
                    return tag.replace('attack.', '').toUpperCase();
                }
            }
            return null;
        },

        mapTacticFromDirectory(directory) {
            const mapping = {
                '02 Resource Development': 'Resource Development',
                '03 Initial Access': 'Initial Access',
                '04 Execution': 'Execution',
                '05 Defense Evasion': 'Defense Evasion',
                '05 Persistence': 'Persistence',
                '06 Privilege Escalation': 'Privilege Escalation',
                '07 Defense Evasion': 'Defense Evasion',
                '08 Credential Access': 'Credential Access',
                '09 Discovery': 'Discovery',
                '10 Lateral Movement': 'Lateral Movement',
                '11 Collection': 'Collection',
                '12 Command and Control': 'Command and Control',
                '13 Exfiltration': 'Exfiltration',
                '14 Impact': 'Impact'
            };
            return mapping[directory] || 'Unknown';
        },

        cleanDescription(description) {
            if (Array.isArray(description)) {
                description = description.join(' ');
            }
            return description.replace(/\s+/g, ' ').trim();
        },

        async discoverYaraRules() {
            // Dynamic basePath for localhost development and GitHub Pages
            const basePath = (() => {
                const isLocalhost = window.location.hostname === 'localhost' ||
                                   window.location.hostname === '127.0.0.1';

                if (isLocalhost) {
                    // For localhost, check if server runs from root or docs/
                    return window.location.pathname.includes('/docs/') ? '' : '/docs';
                } else {
                    // GitHub Pages production - use raw content
                    return 'https://raw.githubusercontent.com/infinit3i/Defensive-Rules/main';
                }
            })();

            const yaraFiles = await this.discoverRuleFiles('Yara');
            console.log(`Discovered ${yaraFiles.length} YARA files`);

            for (const file of yaraFiles) {
                try {
                    const url = `${basePath}/Yara/${encodeURIComponent(file)}`;
                    const response = await fetch(url);
                    if (response.ok) {
                        const yaraContent = await response.text();
                        const rules = this.parseYaraRules(yaraContent, file);
                        this.yaraRules.push(...rules);
                    } else {
                        console.warn(`HTTP ${response.status} for YARA file ${file}`);
                    }
                } catch (error) {
                    console.warn(`Failed to load YARA file ${file}:`, error.message);
                }
            }
        },

        parseYaraRules(yaraContent, filename) {
            const rules = [];
            const ruleMatches = yaraContent.match(/rule\s+(\w+)\s*{([^}]*meta:[^}]*?)}/gs);

            if (ruleMatches) {
                for (const match of ruleMatches) {
                    const nameMatch = match.match(/rule\s+(\w+)/);
                    const metaMatch = match.match(/meta:\s*(.*?)(?=strings:|condition:|$)/s);

                    if (nameMatch && metaMatch) {
                        const ruleName = nameMatch[1];
                        const meta = this.parseYaraMeta(metaMatch[1]);

                        rules.push({
                            name: ruleName,
                            filename: filename,
                            description: meta.description || '',
                            author: meta.author || 'Unknown',
                            severity: meta.severity || 'medium',
                            mitre_attack: meta.mitre_attack || '',
                            reference: meta.reference || ''
                        });
                    }
                }
            }

            return rules;
        },

        parseYaraMeta(metaText) {
            const meta = {};
            const lines = metaText.split('\n');

            for (const line of lines) {
                const cleanLine = line.trim();
                if (cleanLine.includes('=')) {
                    const [key, value] = cleanLine.split('=', 2);
                    const cleanKey = key.trim();
                    const cleanValue = value.trim().replace(/['"]/g, '');
                    meta[cleanKey] = cleanValue;
                }
            }

            return meta;
        },

        extractMetadata() {
            const tacticsSet = new Set();
            const techniquesSet = new Set();

            this.sigmaRules.forEach(rule => {
                if (rule.tactic) tacticsSet.add(rule.tactic);
                if (rule.technique) techniquesSet.add(rule.technique);
            });

            this.yaraRules.forEach(rule => {
                if (rule.mitre_attack) techniquesSet.add(rule.mitre_attack);
            });

            this.tactics = Array.from(tacticsSet).sort();
            this.uniqueTechniques = Array.from(techniquesSet).sort();
        },

        loadSampleData() {
            // Fallback sample data
            this.sigmaRules = [
                {
                    id: 'sample-1',
                    title: 'BOF Process Injection Detection',
                    level: 'high',
                    tactic: 'Defense Evasion',
                    technique: 'T1055',
                    description: 'Detects Beacon Object File process injection techniques',
                    status: 'experimental',
                    author: 'Defensive Rules Project'
                }
            ];

            this.yaraRules = [
                {
                    name: 'BOF_Detection',
                    severity: 'high',
                    mitre_attack: 'T1620',
                    description: 'Detects Beacon Object File binary characteristics'
                }
            ];

            this.extractMetadata();
        },

        isValidHttpUrl(string) {
            // Prevent XSS via javascript: URLs and other dangerous schemes
            try {
                const url = new URL(string);
                return url.protocol === 'http:' || url.protocol === 'https:';
            } catch (_) {
                return false;
            }
        },

        isValidRuleFileName(filename) {
            // Validate filename to prevent path traversal and only allow expected file types
            if (!filename || typeof filename !== 'string') return false;

            // Block dangerous patterns
            if (filename.includes('..') || filename.includes('/') ||
                filename.includes('\\') || filename.includes(':') ||
                filename.includes('<') || filename.includes('>') ||
                filename.includes('|') || filename.includes('*') ||
                filename.includes('?')) {
                return false;
            }

            // Only allow expected file extensions
            return /^[A-Za-z0-9_\[\]\.\-\s]+\.(yml|yaml|yar|yara)$/i.test(filename);
        }
    }
}).mount('#app');